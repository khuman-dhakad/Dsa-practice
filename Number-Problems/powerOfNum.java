 class PowerOfNum {
    public static void main(String[] args) {
        double base = 5.0;
        double exponent = 3.0;
        
        // Calculating the power using Math.pow
        double res = Math.pow(base, exponent);
        
        System.out.println(base + " raised to the power of " + exponent + " is: " + res);
    }
}
